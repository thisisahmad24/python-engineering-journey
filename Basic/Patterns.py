#Logic Building via Patterns and Shapes

# Square Pattern
print('Square Pattern:')
for i in range(3):
    for j in range(3):
        print ('*' , end=" ")
    print()
    end= " "

# Rectangle Pattern
print('Rectangle Pattern:')
for i in range (3):
    for j in range(5):
        print('*' , end=" ")
    print()
    end= " "